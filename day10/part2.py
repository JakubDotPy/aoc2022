EXPECTED = None


def draw_crt(crt: list):
    for start, stop in pairwise(range(0, 241, 40)):
        for c in crt[start:stop]:
            print(c, end='')
        print()


def compute(s: str) -> int:
    lines = s.splitlines()
    cycle = 0
    reg_x = 1

    CRT = list(range(240))

    for line in lines:
        match line.split():
            case ['noop']:
                loop_len, val = 1, 0
            case 'addx', val:
                loop_len, val = 2, int(val)
            case _:
                loop_len, val = 0, 0

        for _ in range(loop_len):
            row_offset = 40 * (cycle // 40)
            sprite_pos = {
                reg_x + row_offset - 1,
                reg_x + row_offset,
                reg_x + row_offset + 1,
            }
            CRT[cycle] = 'â–ˆ' if cycle in sprite_pos else ' '
            cycle += 1
        reg_x += val

    draw_crt(CRT)
    return


@pytest.mark.solved
@pytest.mark.parametrize(
    ('input_s', 'expected'),
    (
            (INPUT_S, EXPECTED),
    ),
)
def test(input_s: str, expected: int) -> None:
    assert compute(input_s) == expected


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument('data_file', nargs='?', default=INPUT_TXT)
    args = parser.parse_args()

    with open(args.data_file) as f, timing():
        print(compute(f.read()))

    return 0


if __name__ == '__main__':
    raise SystemExit(main())
